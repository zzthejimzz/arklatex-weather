// Bake the exact IEM n0q color table into src/map/n0q-lut.js so the radar
// renderer can invert tile RGB back to dBZ. Source of truth is IEM's raster
// metadata page (rid=2 = composite_n0q), which lists every 8-bit index with
// its dBZ value and RGB: index 0 = missing, index 1..255 → dBZ (i-65)/2
// (-32 … 95 in 0.5 steps). Tiles are served as RGBA PNGs, so runtime code
// only ever sees these RGBs — never the indices.
//
// Run: npm run build-radar-lut   (re-run only if IEM ever changes the ramp)
import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const URL = 'https://mesonet.agron.iastate.edu/GIS/rasters.php?rid=2';

const res = await fetch(URL);
if (!res.ok) throw new Error(`HTTP ${res.status}`);
const html = await res.text();

// <tr><td>65</td><td>0</td><td>R</td><td>G</td><td>B</td><td>#hex</td></tr>
const rowRe =
  /<tr><td>(\d+)<\/td><td>(-?[\d.]+|Missing)<\/td><td>(\d+)<\/td><td>(\d+)<\/td><td>(\d+)<\/td>/g;

const entries = []; // [dbz, r, g, b] — index 0 (Missing) skipped
let m;
while ((m = rowRe.exec(html)) !== null) {
  const [, idx, val, r, g, b] = m;
  if (val === 'Missing') continue;
  const dbz = parseFloat(val);
  const expected = (parseInt(idx, 10) - 65) / 2;
  if (dbz !== expected)
    throw new Error(`index ${idx}: dBZ ${dbz} != expected ${expected} — mapping changed?`);
  entries.push([dbz, +r, +g, +b]);
}

if (entries.length !== 255)
  throw new Error(`expected 255 entries, got ${entries.length} — page layout changed?`);

const out = `// GENERATED by scripts/build-radar-lut.js — do not edit by hand.
// IEM composite_n0q lookup table (rasters.php?rid=2): [dBZ, r, g, b] per
// 8-bit index 1..255. dBZ = (index - 65) / 2. Runtime inverts to RGB→dBZ.
export const N0Q_LUT = ${JSON.stringify(entries)};
`;

const dest = join(dirname(fileURLToPath(import.meta.url)), '../src/map/n0q-lut.js');
writeFileSync(dest, out);
console.log(`wrote ${dest} (${entries.length} entries)`);
