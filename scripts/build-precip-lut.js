// Bake the IEM MRMS precipitation color table into src/map/precip-lut.js so
// the rainfall scanner can invert tile RGB back to millimeters. Source of
// truth is IEM's raster metadata page (rid=6 = mrms_p24h); rids 7/8 (p48h,
// p72h) serve the identical ramp, so one table covers all three periods.
// Index 0 = 0 mm (transparent in tiles), 1..3 are trace amounts drawn as
// RGB(0,0,0) — skipped, black is indistinguishable from "nothing" — and
// index 255 = Missing.
//
// Run: npm run build-precip-lut   (re-run only if IEM ever changes the ramp)
import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const URL = 'https://mesonet.agron.iastate.edu/GIS/rasters.php?rid=6';

const res = await fetch(URL);
if (!res.ok) throw new Error(`HTTP ${res.status}`);
const html = await res.text();

// <tr><td>index</td><td>mm</td><td>R</td><td>G</td><td>B</td>…
const rowRe =
  /<tr><td>(\d+)<\/td><td>(-?[\d.]+|Missing)<\/td><td>(\d+)<\/td><td>(\d+)<\/td><td>(\d+)<\/td>/g;

const entries = []; // [mm, r, g, b]
let m;
while ((m = rowRe.exec(html)) !== null) {
  const [, , val, r, g, b] = m;
  if (val === 'Missing') continue;
  const mm = parseFloat(val);
  if (mm <= 0) continue;
  if (+r === 0 && +g === 0 && +b === 0) continue; // trace drawn black — unusable
  entries.push([mm, +r, +g, +b]);
}

if (entries.length < 200)
  throw new Error(`expected ~251 entries, got ${entries.length} — page layout changed?`);

const out = `// GENERATED by scripts/build-precip-lut.js — do not edit by hand.
// IEM mrms_p24h lookup table (rasters.php?rid=6): [mm, r, g, b] per 8-bit
// index. The p48h/p72h rasters share this exact ramp. Runtime inverts RGB→mm.
export const PRECIP_LUT = ${JSON.stringify(entries)};
`;

const dest = join(dirname(fileURLToPath(import.meta.url)), '../src/map/precip-lut.js');
writeFileSync(dest, out);
console.log(`wrote ${dest} (${entries.length} entries)`);
